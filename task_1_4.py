zahl1 = float(input("Gib eine Zahl ein: "))
zahl2 = float(input("Gib eine zweite Zahl ein: "))

print (round(zahl1 + zahl2,2))
print (round(zahl1 - zahl2,2))
print (round(zahl1 / zahl2,2))
print (round(zahl1 * zahl2,2))

# / gibt immer eine Kommazahl zurück, während // das Ergebnis auf die nächste ganze Zahl nach unten abrundet.